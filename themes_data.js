// Function to get theme colors
function getThemeColors(themeName) {
  const theme = figmaThemes[themeName];
  if (!theme) return null;
  return theme.colors;
}

// Function to generate CSS variables from theme colors
function generateThemeCSS(themeName, colors) {
  const cssVars = {};
  
  // Assign colors to CSS variables based on the number of colors available
  if (colors.length >= 1) cssVars['--theme-primary'] = colors[0];
  if (colors.length >= 2) cssVars['--theme-secondary'] = colors[1];
  if (colors.length >= 3) cssVars['--theme-tertiary'] = colors[2];
  if (colors.length >= 4) cssVars['--theme-quaternary'] = colors[3];
  
  // Generate complementary colors for UI elements
  if (colors.length >= 2) {
    cssVars['--calculator-bg'] = colors[0];
    cssVars['--display-bg'] = colors[1];
    cssVars['--button-bg'] = colors[0];
    cssVars['--button-hover'] = colors[1];
    cssVars['--display-text'] = getContrastColor(colors[1]);
    cssVars['--button-text'] = getContrastColor(colors[0]);
  }
  
  return cssVars;
}

// Helper function to get contrasting color
function getContrastColor(hexColor) {
  // Convert hex to RGB
  const r = parseInt(hexColor.slice(1, 3), 16);
  const g = parseInt(hexColor.slice(3, 5), 16);
  const b = parseInt(hexColor.slice(5, 7), 16);
  
  // Calculate luminance
  const luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  
  // Return black or white based on luminance
  return luminance > 0.5 ? '#000000' : '#FFFFFF';
}

